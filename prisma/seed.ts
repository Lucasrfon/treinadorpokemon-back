const pokeData: Pokemon[] = [];

const eventCardData: EventCard[] = [];

const itemCardData: ItemCard[] = [];

for (let i = 0; i < pokeNames.length; i++) {
	pokeData.push({
		id: i + 1,
		name: pokeNames[i],
		color: pokeColors[i],
	});

	if (i < eventCard.length) {
		eventCardData.push({
			id: i + 1,
			name: eventCard[i],
		});
	}

	if (i < itemCard.length) {
		itemCardData.push({
			id: i + 1,
			name: itemCard[i],
		});
	}
}

export async function main() {
	await prisma.pokemon.createMany({
		data: pokeData,
		skipDuplicates: true,
	});
	await prisma.eventCard.createMany({
		data: eventCardData,
		skipDuplicates: true,
	});
	await prisma.itemCard.createMany({
		data: itemCardData,
		skipDuplicates: true,
	});
}

main()
	.catch((e) => {
		console.log(e);
		process.exit(1);
	})
	.finally(() => {
		prisma.$disconnect();
	});
